def pluse(a=0, b=0):
  print(a + b)

def minus(a=0, b=0):
  print(a - b)

def multiplication(a=0, b=0):
  print(a * b)

def division(a=0, b=1):
  print(a / b)

def power_of(a=0, b=1):
  print(a**b)

